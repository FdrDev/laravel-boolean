  <h2 class="font-weight-bold">Prima del corso</h2>
  <h4>Chi può fare il corso Boolean?</h4><span>+</span>
  <p>Chiunque abbia una forte curiosità verso il mondo della programmazione e voglia di imparare a fare il mestiere di sviluppatore. Età e curriculum non sono una discriminante: abbiamo avuto studenti dai 18 ai 45 anni, dal ragazzo senza esperienza lavorativa al professionista affermato che vuole cambiare settore. Se non hai mai programmato o se hai già seguito qualche corso ma senti di non aver approfondito abbastanza per essere autonomo, Boolean è il corso che fa per te. </p>

  <h4>Che competenze devo avere per iniziare il corso?</h4><span>+</span>
  <p>Il corso parte da zero quindi non serve alcuna competenza di programmazione; è necessario però superare il processo di selezione.</p>

  <h4>Questo corso è una valida alternativa alla laurea?</h4><span>+</span>
  <p>Lo è sicuramente in quanto metodo alternativo per accedere al mondo del lavoro: le aziende che cercano sviluppatori sanno che una laurea non è garanzia di saper programmare e sono più che disposte a valutare candidati che non hanno frequentato una università. Non ci sono percorsi di carriera nel mondo della programmazione che ti sono preclusi se non hai una laurea.</p>

  <h4>C'è un processo di selezione?</h4><span>+</span>
  <p>Si, Boolean è un corso intensivo che richiede impegno costante, dedizione e una attitudine al problem solving. Per assicurarci che ogni studente abbia concrete possibilità di diventare uno sviluppatore e iniziare una nuova carriera, selezioniamo gli studenti tramite un test di logica a risposta multipla e un colloquio motivazionale. Contattaci per iniziare il tuo percorso o saperne di più!</p>

